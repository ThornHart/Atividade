public class Pessoa {

    private int age;
    private String name;
    private int cpf;

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public int getCpf() {
        return cpf;
    }

    public void setCpf(int cpf) {
        this.cpf = cpf;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }


}
